def env_bool(name: str, default: bool = False) -> bool:
    v = os.getenv(name)
    if v is None:
        return default
    return v.strip().lower() in ("1", "true", "yes", "on")

def env_list(name: str, default=None, sep=","):
    v = os.getenv(name)
    if not v:
        return default or []
    return [item.strip() for item in v.split(sep) if item.strip()]

